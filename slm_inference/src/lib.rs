//! High-level abstractions for running quantised small language models (SLMs) locally.
//!
//! The crate is structured as a stack of composable traits:
//!
//! * **Token layer** – [`SlmToken`], [`SlmVocab`]: encode/decode between strings and token IDs.
//! * **Hardware layer** – [`SlmContext`], [`SlmBatch`]: KV-cache management and batched decoding.
//! * **Model loading** – [`SlmModelConfig`], [`SlmModel`], [`SlmHfModel`]: load GGUF weights, optionally from HuggingFace.
//! * **Generation loop** – [`SlmInference`], [`SlmSimpleInference`]: prefill and autoregressive sampling.
//! * **Chat formatting** – [`SlmFormatter`], [`SlmDynamicFormatter`]: model-family chat templates.
//! * **Conversation layer** – [`SlmOracle`], [`SlmSimpleOracle`]: turn-taking, context save/restore, and JSON-guided generation.
//! * **Constrained decoding** – [`SlmConstraint`], [`SlmSimpleTokEnv`]: grammar-gated token sampling.

